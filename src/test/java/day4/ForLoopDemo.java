package day4;

public class ForLoopDemo {

	public static void main(String[] args) {
		
		/*
		 *  for(init;condition;increment/decrement)
		 *  {
		 *  
		 *  	
		 *  
		 *  }
		 * 
		 * 
		 */
		
		System.out.println("Start");
		
		for(int i=0;i<5;i++) 
		{	
			String msg="Hello";
			
			System.out.println(msg);
		}

		
		System.out.println("End");
		
		
		
		
		

	}

}
